        Object executaOMunda = mundo.executaOMunda(jTable_mapa,jTable_visaoColetorTotal);
        if (executaOMunda instanceof Coletor) {
            atualizaDadosDoColetor((Coletor) executaOMunda);
        }
//            atualizaVisao();
