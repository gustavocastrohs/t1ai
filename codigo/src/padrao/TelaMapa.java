    /**
     *
     * @param x tamanho do mundo
     * @param y tamanho do mundo
     * @param quantidadeDeLixoEsperada quantidade de lixo que será gerada
     * randomicamente
     * @param quantidadeDeRecargas quantidade de recargas
     * @param quantidadeDeLixeirasEsperado
     * @param capacidadeDaLixeira
     * @param tamanhoVisaoDoColetor tamanho da visão dos coletores
     * @throws java.lang.Exception
     */
    private int tamanhoDoX;
    private int tamanhoDoY;
    
    public void criaMundo(int x, int y, int quantidadeDeLixoEsperada, int quantidadeDeRecargas, int quantidadeDeLixeirasEsperado, int capacidadeDaLixeira, int tamanhoVisaoDoColetor) throws Exception {
        
        tamanhoDoX = x;
        tamanhoDoY = y;
//Mundo(ix, y, quantidadeDeLixoEsperada, quantidadeDeRecargas,quantidadeDeLixeirasEsperado,capacidadeDaLixeira,tamanhoVisaoDoColetor) {
        try {
            mundo = new Mundo(x, y, quantidadeDeLixoEsperada, quantidadeDeRecargas, quantidadeDeLixeirasEsperado, capacidadeDaLixeira, tamanhoVisaoDoColetor);
            DefaultTableModel model = new DefaultTableModel(x, y);
            jTable_mapa.setModel(model);
            atualizaVisao();
        } catch (Exception e) {
            throw new Exception("Algum campo está incompleto ou errado");
        }
    }
    
    public void criaUmColetor(int capacidadeDaLixeiraMaxima, int energiaMinima, int energiaMaxima) throws Exception {
        try {
            Coletor criaUmColetor = mundo.criaUmColetor(capacidadeDaLixeiraMaxima, energiaMinima, energiaMaxima,jTable_visaoColetorTotal,jTable_visaoColetorParcial);
            JOptionPane.showMessageDialog(rootPane, "Seu novo coletor foi criado em : " + criaUmColetor.getxAtual() + " / " + criaUmColetor.getyAtual());
            atualizaVisao();
        } catch (Exception e) {
            throw new Exception("Algum campo está incompleto ou errado");
        }
    }
    
